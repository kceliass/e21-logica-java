    private void dtnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dtnCalcularActionPerformed
        // TODO add your handling code here:
        if(!"".equals(txtNome.getText()) &&
           !"".equals(txtPeso.getText().replace(",",".")) &&
           !"".equals(txtAltura.getText().replace(",","."))) {
            double altura, peso, imc;
            String textoPanel = tpanelResultado.getText() + txtNome.getText() + " - IMC Ã©: " ;
            altura = Double.parseDouble(txtAltura.getText());
            peso = Double.parseDouble(txtPeso.getText());
            imc = peso / (altura*altura);
            
            textoPanel += imc + "\n";
            tpanelResultado.setText(textoPanel);
            
            //limpar os campos
            limparCampos();
         
        } else {
            JOptionPane.showMessageDialog(this, "Favor preencher todos os campos","Campos em branco", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_dtnCalcularActionPerformed

